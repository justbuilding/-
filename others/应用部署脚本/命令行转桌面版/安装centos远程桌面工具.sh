echo "*******************************************************************************"
echo " 判断防火墙为是否为iptable，是则开放端口3389，否则输出iptable没运行"
echo "*******************************************************************************"
systemctl  status  iptables.service>/dev/null 2>&1
if [ $? -ne 0 ]; then
	echo "*******************************************************************************"
	echo " 配置防火墙开放3389端口"
	echo "*******************************************************************************"
	iptables -A INPUT -p tcp --dport 3389 -j ACCEPT
	iptables -A OUTPUT -p tcp --sport 3389 -j ACCEPT
	iptables -A INPUT -p tcp -s 192.168.0.41 -j ACCEPT
	iptables -A OUTPUT -p tcp -s 192.168.0.41 -j ACCEPT
	service  iptables save 
	systemctl  restart  iptables.service
	echo "*******************************************************************************"
	echo " 配置防火墙开放3389端口成功"
	echo " 临时关闭防火墙可以使用    systemctl  stop  iptables.service"
	echo " 禁止开机启动（需重启生效） sed -i '7s/enforcing/disabled/' /etc/selinux/config"
	echo " 查看防火墙规则 iptables -L -n "
	echo "*******************************************************************************"
else
    echo "*******************************************************************************"
	echo "iptable没运行"
	echo "*******************************************************************************"
fi
